def draws_consumed(before: RngStreamState, after: RngStreamState) -> int:
    """Count raw draws separating two snapshots of the same stream."""
    clone = RngStream.restore(before)
    count = 0
    while clone.export_state() != after:
        clone.next_uint64()
        count += 1
        assert count <= 10_000, "snapshots are not on the same stream"
    return count


class TestGoldenVectors:
    def test_pcg_c_reference_known_answers(self):
        stream = RngStream(*PCGC_REFERENCE_SEED)
        assert [stream.next_uint64() for _ in PCGC_REFERENCE_OUTPUTS] == PCGC_REFERENCE_OUTPUTS

    @pytest.mark.parametrize(("state", "inc", "expected"), NUMPY_OUTPUT_VECTORS)
    def test_numpy_output_vectors(self, state, inc, expected):
        stream = RngStream.restore(RngStreamState(state=state, inc=inc))
        assert [stream.next_uint64() for _ in expected] == expected

    @pytest.mark.parametrize(("entropy", "initstate", "initseq", "state", "inc", "expected"), NUMPY_INIT_VECTORS)
    def test_numpy_init_vectors(self, entropy, initstate, initseq, state, inc, expected):
        stream = RngStream(initstate, initseq)
        snapshot = stream.export_state()
        assert (snapshot.state, snapshot.inc) == (state, inc)
        assert [stream.next_uint64() for _ in expected] == expected

    @pytest.mark.parametrize(("master_seed", "key", "initstate", "initseq"), SHA256_DERIVATION_VECTORS)
    def test_sha256_derivation_vectors(self, master_seed, key, initstate, initseq):
        assert derive_init_pair(master_seed, key) == (initstate, initseq)
        # Independent recomputation of the pinned construction.
        digest = hashlib.sha256(master_seed.to_bytes(16, "big") + b":" + key.encode("utf-8")).digest()
        assert int.from_bytes(digest[:16], "big") == initstate
        assert int.from_bytes(digest[16:], "big") == initseq

    def test_derived_stream_raw_goldens(self):
        stream = RngStream.from_seed_material(42, "combat")
        assert [stream.next_uint64() for _ in DERIVED_STREAM_RAW_GOLDENS] == DERIVED_STREAM_RAW_GOLDENS


class TestStreamDerivation:
    def test_same_seed_same_key_identical_sequences(self):
        a = RngStreams(master_seed=42).get("combat")
        b = RngStreams(master_seed=42).get("combat")
        assert [a.next_uint64() for _ in range(100)] == [b.next_uint64() for _ in range(100)]

    def test_reproducibility_contract_with_dice(self):
        # The definition-of-done demonstration: two independently derived streams from
        # the same master seed and key produce identical 2d6×10 roll sequences.
        streams_a = RngStreams(master_seed=42)
        streams_b = RngStreams(master_seed=42)
        rolls_a = [roll("2d6×10", streams_a.get("treasure")).total for _ in range(10)]
        rolls_b = [roll("2d6×10", streams_b.get("treasure")).total for _ in range(10)]
        assert rolls_a == rolls_b

    def test_different_keys_are_independent(self):
        # Drawing from one stream never shifts another.
        undisturbed = RngStreams(master_seed=42)
        expected = [undisturbed.get("treasure").next_uint64() for _ in range(20)]

        disturbed = RngStreams(master_seed=42)
        for _ in range(100):
            disturbed.get("combat").next_uint64()
        treasure = disturbed.get("treasure")
        drawn = []
        for _ in range(10):
            drawn.append(treasure.next_uint64())
            disturbed.get("combat").next_uint64()  # interleaved draws change nothing
        drawn += [treasure.next_uint64() for _ in range(10)]
        assert drawn == expected

    def test_different_keys_differ(self):
        streams = RngStreams(master_seed=42)
        assert streams.get("combat").next_uint64() != streams.get("treasure").next_uint64()

    def test_container_returns_same_stream_object(self):
        streams = RngStreams(master_seed=42)
        assert streams.get("combat") is streams.get("combat")

    def test_master_seed_property(self):
        assert RngStreams(master_seed=42).master_seed == 42

    @pytest.mark.parametrize("bad_seed", [-1, 1 << 128])
    def test_master_seed_out_of_range_raises(self, bad_seed):
        with pytest.raises(ValueError):
            RngStreams(master_seed=bad_seed)
        with pytest.raises(ValueError):
            derive_init_pair(bad_seed, "combat")

    @pytest.mark.parametrize("bad_value", [-1, 1 << 128])
    def test_init_pair_out_of_range_raises(self, bad_value):
        with pytest.raises(ValueError):
            RngStream(bad_value, 0)
        with pytest.raises(ValueError):
            RngStream(0, bad_value)


class TestRandbelow:
    @pytest.mark.parametrize(("key", "bound", "expected"), RANDBELOW_GOLDENS)
    def test_randbelow_goldens(self, key, bound, expected):
        stream = RngStream.from_seed_material(42, key)
        assert [stream.randbelow(bound) for _ in expected] == expected

    def test_rejection_branch(self):
        # Constructed vector: for master seed 42, key "reject-0", the first two raw
        # draws' top 5 bits are >= 20, so randbelow(20) rejects twice and consumes
        # three draws. The algorithm's most fragile path must not ship untested.
        probe = RngStream.from_seed_material(42, "reject-0")
        candidates = [probe.next_uint64() >> 59 for _ in range(3)]
        assert candidates[0] >= 20, "vector no longer exercises the rejection branch"
        assert candidates[1] >= 20, "vector no longer exercises the rejection branch"
        assert candidates[2] < 20

        stream = RngStream.from_seed_material(42, "reject-0")
        before = stream.export_state()
        assert stream.randbelow(20) == candidates[2] == 11
        assert draws_consumed(before, stream.export_state()) == 3

    def test_randbelow_one_always_zero_and_consumes_one_draw(self):
        stream = RngStream.from_seed_material(42, "ones")
        for _ in range(10):
            before = stream.export_state()
            assert stream.randbelow(1) == 0
            assert draws_consumed(before, stream.export_state()) == 1

    def test_power_of_two_bounds_never_reject(self):
        stream = RngStream.from_seed_material(42, "pow2")
        for bound in (2, 4, 8, 16):
            for _ in range(50):
                before = stream.export_state()
                assert 0 <= stream.randbelow(bound) < bound
                assert draws_consumed(before, stream.export_state()) == 1

    @pytest.mark.parametrize("bad_bound", [0, -1, -20])
    def test_nonpositive_bound_raises(self, bad_bound):
        stream = RngStream.from_seed_material(42, "bad")
        with pytest.raises(ValueError):
            stream.randbelow(bad_bound)

    def test_chi_square_uniformity(self):
        # Fixed seed so CI never flakes — this catches masking bugs, not cosmic rays.
        # Critical value for df=19 at p=0.001 is 43.82; the observed statistic for this
        # seed is ~13.1.
        stream = RngStream.from_seed_material(2025, "chi-square")
        bins = 20
        n = 10_000
        counts = [0] * bins
        for _ in range(n):
            counts[stream.randbelow(bins)] += 1
        expected = n / bins
        chi2 = sum((count - expected) ** 2 / expected for count in counts)
        assert chi2 < 43.82


class TestStateExportRestore:
    def test_roundtrip_continues_sequence(self):
        stream = RngStream.from_seed_material(42, "combat")
        for _ in range(17):
            stream.next_uint64()
        snapshot = stream.export_state()
        expected = [stream.next_uint64() for _ in range(20)]
        restored = RngStream.restore(snapshot)
        assert [restored.next_uint64() for _ in range(20)] == expected

    def test_snapshot_json_roundtrip(self):
        stream = RngStream.from_seed_material(42, "combat")
        stream.next_uint64()
        snapshot = stream.export_state()
        revived = RngStreamState.model_validate_json(snapshot.model_dump_json())
        assert revived == snapshot
        expected = stream.next_uint64()
        assert RngStream.restore(revived).next_uint64() == expected

    def test_export_does_not_disturb_stream(self):
        a = RngStream.from_seed_material(42, "combat")
        b = RngStream.from_seed_material(42, "combat")
        a.export_state()
        assert a.next_uint64() == b.next_uint64()

    def test_snapshot_rejects_even_inc(self):
        with pytest.raises(ValidationError):
            RngStreamState(state=0, inc=2)

    @pytest.mark.parametrize("field", ["state", "inc"])
    @pytest.mark.parametrize("bad_value", [-1, 1 << 128])
    def test_snapshot_rejects_out_of_range(self, field, bad_value):
        values = {"state": 0, "inc": 1}
        values[field] = bad_value
        with pytest.raises(ValidationError):
            RngStreamState(**values)
