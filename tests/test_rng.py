"""Tests for osrlib.core.rng — the determinism contract.

Golden-vector provenance
------------------------

Three independent anchors validate the PCG64 implementation, so no single oracle is
load-bearing:

- ``PCGC_REFERENCE_OUTPUTS``: known-answer values from O'Neill's pcg-c reference
  implementation, ``test-high/expected/check-pcg64.out`` Round 1 (the ``64bit:`` line),
  produced by ``pcg64_srandom_r(&rng, 42u, 54u)`` followed by six ``pcg64_random_r``
  calls (seed constants from ``test-high/check-pcg64.c``, ``XX_SRANDOM_SEEDCONSTS``).
  https://github.com/imneme/pcg-c, retrieved 2026-07-03.
- ``NUMPY_OUTPUT_VECTORS``: numpy's PCG64 handed raw ``(state, inc)`` pairs through its
  ``.state`` property, outputs via ``random_raw()`` — canonical step/output vectors.
- ``NUMPY_INIT_VECTORS``: numpy's real ``pcg64_srandom_r`` exercised through
  ``PCG64(SeedSequence(entropy))``, with the ``(initstate, initseq)`` pair recovered by
  calling ``SeedSequence.generate_state(4, uint64)`` ourselves and assembling
  ``initstate = (w0 << 64) | w1``, ``initseq = (w2 << 64) | w3``.

The numpy vectors were generated once by ``tests/generate_rng_vectors.py`` (run via
``uv run --with numpy``; numpy is not a project dependency) with numpy 2.5.0.

``SHA256_DERIVATION_VECTORS``, the ``randbelow`` goldens, and the rejection-branch
vector were generated from this implementation once its raw output, init, and
derivation paths were validated against the anchors above; they lock behavior against
accidental change rather than validating it against an external oracle.
"""

